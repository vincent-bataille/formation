# méthodes sur des chaines de caractères
print("stop".upper())
print("GO".lower())
print("ceci est une phrase.".capitalize())
print("rue de la joie de vivre".title())

# remplacer une élément par un autre
print("javascript".replace("java","VB"))
print("javascript".replace("java",""))

#vérifier qu'une chaine ne contient que des chiffres
print("100".isdigit())

# vérifier fin ou début
print("photo.jpg".endswith("jpg"))
print("photo.jpg".startswith("photo"))
